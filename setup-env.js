  'RATE_LIMIT_MAX_REQUESTS',
  'DEFAULT_URL_EXPIRY_DAYS',
  'LOG_LEVEL',
  'HELMET_CSP_ENABLED',
  'CORS_ORIGIN',
  'DB_MAX_POOL_SIZE',
  'DB_MIN_POOL_SIZE',
  'SMTP_HOST',
  'SMTP_PORT',
  'SMTP_USER',
  'SMTP_PASS',
  'GOOGLE_ANALYTICS_ID',
  'REDIS_URL',
  'GOOGLE_CLIENT_ID',
  'GOOGLE_CLIENT_SECRET',
  'GITHUB_CLIENT_ID',
  'GITHUB_CLIENT_SECRET',
  'CLOUDINARY_CLOUD_NAME',
  'CLOUDINARY_API_KEY',
